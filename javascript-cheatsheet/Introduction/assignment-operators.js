// An assignment operator assigns a value to its left operand based on the value of its right operand. Here are some of them:


	let number = 50;
	number += 10;
	console.log(number);
// Prints 60

	number -= 10;
	console.log(number);
// Prints 50

	number *= 2;
	console.log(number);
// Prints 100

	number /= 5;
	console.log(number);
// Prints 20

