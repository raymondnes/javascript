// 	Control Flow


// Control flow is the order in which statements are executed in a program. The default control flow is for statements to be read and executed in order from left-to-right, top-to-bottom in a program file. Control structures such as conditionals ( if statements and the like) alter control flow by only executing blocksof code if certain conditions are met. These structures essentially allow a program to make decisions about which code is executed as the program runs.
